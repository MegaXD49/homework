# รับข้อมูลอุณหภูมิจากผู้ใช้ (ให้ใส่เฉพาะตัวเลข)
while True:
    try:
        C = float(input("ป้อนอุณหภูมิ (องศาเซลเซียส): "))
        break  # หลุดจากลูป while ถ้ารับค่าได้ถูกต้อง
    except ValueError:
        print("กรุณาป้อนตัวเลขเท่านั้น")

# คำนวณค่าองศาฟาเรนไฮต์
F = 9/5 * C + 32

# คำนวณค่าเคลวิน
K = C + 273.15

# แสดงผลลัพธ์ในรูปแบบที่ต้องการ
print(f"{C:.2f} องศาเซลเซียส มีค่าเท่ากับ\n{F:.2f} องศาฟาเรนไฮต์ {K:.2f} เคลวิน")
