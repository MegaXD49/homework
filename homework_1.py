# รับข้อมูลอุณหภูมิจากผู้ใช้
celsius = float(input("ป้อนอุณหภูมิ (องศาเซลเซียส): "))

# คำนวณค่าองศาฟาเรนไฮต์
fahrenheit = 9/5 * celsius + 32

# คำนวณค่าเคลวิน
kelvin = celsius + 273.15

# แสดงผลลัพธ์ในรูปแบบที่ต้องการ
print(f"{celsius:.2f} องศาเซลเซียส\n{fahrenheit:.2f} องศาฟาเรนไฮต์\n{kelvin:.2f} เคลวิน")
